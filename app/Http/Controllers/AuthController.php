    public function auth(Request $request) {
        $request->validate([
            "email" => "required",
            "password" => "required"
        ]);

        $user = User::where("email", $request->email)->first();
        
        if($user && Hash::check($request->password, $user->password)) {
            return $user;
        }

        return "nao existe";
    }

